
// Immutable objects and final keyword
// String objects are immutable
// Immutable object does not allow the data inside the object to change.
// Final does not allow the object to point to another object.
// Static variable = constant

public class Example_21{
	public static final String MULTIPY = "multiply";
	public static void main(String[] args){
		System.out.println("The operation is "+ MULTIPLY);
	}
}
